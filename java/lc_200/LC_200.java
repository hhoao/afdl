//我的代码
class Solution {
    private int sum;
    public int numIslands(char[][] grid) {
        this.sum = 0;
        int n = grid.length, m = grid[0].length;
        for (int i = 0; i < n; i++){
            for (int j = 0; j < m; j++){
                if (grid[i][j] == '1'){
                    sum++;
                    dfs(grid, i, j);
                }
            }
        }
        return sum;
    }
    public void dfs(char[][] grid, int x, int y){
        if (x < 0 || x >= grid.length) return ;
        if (y < 0 || y >= grid[0].length) return;
        if (grid[x][y] == '2' || grid[x][y] == '0') return;
        if (grid[x][y] == '1'){
            grid[x][y] = '2';
        }
        dfs(grid, x + 1, y);
        dfs(grid, x - 1, y);
        dfs(grid, x, y+1);
        dfs(grid, x, y-1);
    }
}
