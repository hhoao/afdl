class Solution1 {
    public int maxRotateFunction(int[] nums) {
        int n = nums.length;
        long ans = -2147483648;
        long sum = 0, total = 0;
        for (int i = 0; i < n; i++){
            sum += nums[i];
            total = total + i * nums[i];
        }
        for (int i = 0; i < n; i++){
            ans = Math.max(ans, total);
            total += sum;
            total -= n * nums[n - i - 1];
        }
        return (int)ans;
    }
}
