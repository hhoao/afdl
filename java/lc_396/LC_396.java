class Solution {
    public int maxRotateFunction(int[] nums) {
        int n = nums.length;
        long ans = -2147483648;
        for (int i = 0; i < n; i++){
            long temp = 0L;
            for (int j = 0, k = i; j < n; j++, k++){
                temp += j * nums[k % n];
            }
            ans = Math.max(temp, ans);
        }
        return (int)ans;
    }
}
